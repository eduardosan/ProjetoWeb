<?php

namespace Grupo\ProjetoBundle\Entity;

/**
 * Relatos
 */
class Relatos
{
    /**
     * @var integer
     */
    private $id;


    /**
     * @var string
     */
    private $cidade;
    /**
     * @var string
     */
    private $pais;
    /**
     * @var string
     */
    private $autor;

    /**
     * @var \DateTime
     */
    private $data;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

  
    /**
     * Set cidade
     *
     * @param string $cidade
     *
     * @return Relatos
     */
    public function setCidade($cidade)
    {
        $this->cidade = $cidade;

        return $this;
    }

    /**
     * Get cidade
     *
     * @return string
     */
    public function getCidade()
    {
        return $this->cidade;
    }

    /**
     * Set autor
     *
     * @param string $autor
     *
     * @return Relatos
     */
    public function setAutor($autor)
    {
        $this->autor = $autor;

        return $this;
    }

    /**
     * Get autor
     *
     * @return string
     */
    public function getAutor()
    {
        return $this->autor;
    }

    /**
     * Set data
     *
     * @param \DateTime $data
     *
     * @return Relatos
     */
    public function setData($data)
    {
        $this->data = $data;

        return $this;
    }

    /**
     * Get data
     *
     * @return \DateTime
     */
    public function getData()
    {
        return $this->data;
    }

    /**
     * Set pais
     *
     * @param string $pais
     *
     * @return Relatos
     */
    public function setPais($pais)
    {
        $this->pais = $pais;

        return $this;
    }

    /**
     * Get pais
     *
     * @return string
     */
    public function getPais()
    {
        return $this->pais;
    }
    /**
     * @var string
     */
    private $novoRelato;


    /**
     * Set novoRelato
     *
     * @param string $novoRelato
     *
     * @return Relatos
     */
    public function setNovoRelato($novoRelato)
    {
        $this->novoRelato = $novoRelato;

        return $this;
    }

    /**
     * Get novoRelato
     *
     * @return string
     */
    public function getNovoRelato()
    {
        return $this->novoRelato;
    }
    /**
     * @var image
     */
    private $imagem;


    /**
     * Set imagem
     *
     * @param \image $imagem
     *
     * @return Relatos
     */
    public function setImagem(\image $imagem)
    {
        $this->imagem = $imagem;

        return $this;
    }

    /**
     * Get imagem
     *
     * @return \image
     */
    public function getImagem()
    {
        return $this->imagem;
    }
}
